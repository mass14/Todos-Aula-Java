package trabalhoUm;

public class Variaveis {

	public static void main(String[] args) {
		// crie uma variável do tipo inteiro chamada 'i' que possui como valor
		// 39
		int i = 39;

		TestValores.correcao(i, 39);

		// crie uma variável do tipo booleano chamada 'b' que receba o valor
		// true
		boolean b = true;

		TestValores.correcao(b, true);

		// troque o valor de b para false
		b = false;

		TestValores.correcao(b, false);

		// crie uma variável do tipo double chamada 'd' que receba o valor
		// 4,32112
		double d = 4.32112;

		TestValores.correcao(d, 4.32112);

		// crie uma variável do tipo float chamada 'f' que receba o valor 4,324
		float f = 4.324f;

		TestValores.correcao(f, 4.324f);

		// crie uma variável do tipo String chamada 's' que receba o valor
		// "Piece of cake"
		String s = "Piece of cake";

		TestValores.correcao(s, "Piece of cake");
	}

}
